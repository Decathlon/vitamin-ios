public struct VitaminImageAsset {
  public fileprivate(set) var name: String

  public var image: UIImage {
    let bundle = BundleToken.bundle
    #if os(iOS) || os(tvOS)
    let resultImage = UIImage(named: name, in: bundle, compatibleWith: nil)
    #elseif os(watchOS)
    let resultImage = UIImage(named: name)
    #endif
    guard let result = resultImage else {
      fatalError("Unable to load image asset named \(name).")
    }
    return result
  }
}

public extension UIImage {
  convenience init?(asset: VitaminImageAsset) {
    #if os(iOS) || os(tvOS)
    let bundle = BundleToken.bundle
    self.init(named: asset.name, in: bundle, compatibleWith: nil)
    #elseif os(watchOS)
    self.init(named: asset.name)
    #endif
  }
}

